/*  Q6. Write a C++ program to print an int, a double and a char on screen  */

#include <iostream>

using namespace std;

int main()
{
    // declaring variables
    int x = 14;
    double y = 3.14;
    char ch = 'C';

    // printing variables values
    cout<<"Integer : "<<x<<endl;
    cout<<"Double : "<<y<<endl;
    cout<<"Character : "<<ch<<endl;
    return 0;
} 
