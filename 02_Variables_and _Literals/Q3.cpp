/*  Q3. Write a C++ program to take two integer inputs from user and print sum and product of them. */

#include <iostream>

using namespace std;

int main()
{
    int x, y;        

    // taking input from user
    cout<<"Enter integer 1 : ";
    cin>>x;
    cout<<"Enter integer 2 : ";
    cin>>y;
    
    // printing the sum and product
    cout<<"Sum : "<<x+y<<endl;
    cout<<"Product : "<<x*y<<endl;
    
    return 0;
} 